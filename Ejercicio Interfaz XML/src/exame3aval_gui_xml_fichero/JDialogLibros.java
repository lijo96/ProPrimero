    private void jButtonAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnteriorActionPerformed
        pos--;
        mostrarDatosLibro(pos);
        if (pos == 0) {
            jButtonAnterior.setEnabled(false);
        }
        if (pos <= (libros.size() - 1)) {
            jButtonSeguinte.setEnabled(true);
        }
        
    }//GEN-LAST:event_jButtonAnteriorActionPerformed

    private void jButtonMercarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMercarActionPerformed
        try {
            int unid = Integer.valueOf(jTextFieldUnidades.getText());
            if (unid > 0) {
                compra.add(new Pedido(libros.get(pos), unid));
                jButtonRematarCompra.setEnabled(true);
            }
        } catch (NumberFormatException e) {
            
        }
        
    }//GEN-LAST:event_jButtonMercarActionPerformed

    private void jButtonVolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVolverActionPerformed
        compra.clear();
        libros.clear();

        this.setVisible(false);
    }//GEN-LAST:event_jButtonVolverActionPerformed

    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        if (libros.size() >= 1 ) {
            mostrarDatosLibro(pos);
            if (libros.size() > 1) {
                jButtonSeguinte.setEnabled(true);
            }
            jButtonMercar.setEnabled(true);
        }
    }//GEN-LAST:event_formWindowGainedFocus

    private void jButtonSeguinteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeguinteActionPerformed
        pos++;
        mostrarDatosLibro(pos);
        if (pos == (libros.size() - 1)) {
            jButtonSeguinte.setEnabled(false);
        }
        if (pos >= 0) {
            jButtonAnterior.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonSeguinteActionPerformed


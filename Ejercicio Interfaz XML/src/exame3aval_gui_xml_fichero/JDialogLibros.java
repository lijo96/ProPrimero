    private void mostrarDatosLibro(int posicion) {
        int curso;
        Iterator it;
        Libro libro = libros.get(posicion);
        jComboBoxAutores.removeAllItems();
        jTextFieldTitulo.setText(libro.getTitulo());
        jTextFieldEditor.setText(libro.getEditor());
        jTextFieldPax.setText(Integer.toString(libro.getPax()));
        it = libro.getAutores().iterator();
        for (; it.hasNext();) {
            jComboBoxAutores.addItem((String) it.next());
        }
        jTextFieldPrezo.setText(Float.toString(libro.getPrezo()));
        jTextFieldXenero.setText(libro.getXenero());
        buttonGroupPuntuacion.clearSelection();
        switch (libro.getPuntuacion()) {
            case 1:  
                jRadioButton1.setSelected(true);
                break;
            case 2: jRadioButton2.setSelected(true);
                break;
            case 3: jRadioButton3.setSelected(true);
                break;
            case 4: jRadioButton4.setSelected(true);
                break;
            case 5: jRadioButton5.setSelected(true);
                break;
                
        }
        jTextFieldUnidades.setText("");
    }

    

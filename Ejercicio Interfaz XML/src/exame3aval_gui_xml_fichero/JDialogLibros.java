    private void jButtonRematarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRematarCompraActionPerformed

        try (BufferedWriter buffer = new BufferedWriter(
                new FileWriter("pedidos.txt", false))){
            Iterator it;
            it = compra.iterator();
            while (it.hasNext()) {
                Pedido pedido = (Pedido)it.next();
                Libro libro = pedido.getLibro();
                buffer.write(libro.getTitulo() 
                        + ";" + (libro.getPrezo() * pedido.getUnidades()));
                buffer.newLine();
            }
        } catch (IOException e) {
            System.out.println("ERRO: Ocorreu un erro na lectura do ficheiro");
        }
        this.setVisible(false);
    
    }//GEN-LAST:event_jButtonRematarCompraActionPerformed

